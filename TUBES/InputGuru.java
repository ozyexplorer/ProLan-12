public class InputGuru {

    public String nama;
    public String alamat;
    public int umur;
    private String nipGuru;
    private int guruKelas;
    
    
    public String getNamaGuru()
    {
        return nama;
    }
    
    public void setNamaGuru(String nama)
    {
        this.nama = nama;
    }
    
    public String getNipGuru()
    {
        return nipGuru;
    }
    
    public void setNipGuru(String nipGuru)
    {
        this.nipGuru = nipGuru;
    }
    
    public int getKelasGuru()
    {
        return guruKelas;
    }
    
    public void setKelasGuru(int guruKelas)
    {
        this.guruKelas = guruKelas;
    }
    
    public int getUmurGuru()
    {
        return umur;
    }
    
    public void setUmurGuru(int umur)
    {
        this.umur = umur;
    }
    
    public String getAlamatGuru()
    {
        return alamat;
    }
    
    public void setAlamatGuru(String alamat)
    {
        this.alamat = alamat;
    }
    
    
    
}
